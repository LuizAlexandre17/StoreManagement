    private void jButton1ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton1ActionPerformed
        FileOutputStream escritorArquivos = null;
        try {
            try {
                escritorArquivos = new FileOutputStream("lang.cfg", false);
            } catch (FileNotFoundException ex) {
                Logger.getLogger(Config.class.getName()).log(Level.SEVERE, null, ex);
            }
            String msg = (String) jComboBox1.getSelectedItem();
            int tamanho = 0;
            for (String name : Vars.LANG_LIST_FULLNAME) {
                if (!name.equals(msg)) {
                    tamanho += 1;
                } else {
                    break;
                }
            }
            msg = Vars.LANG_LIST[tamanho];
            tamanho = 0;
            while (tamanho < msg.length()) {
                escritorArquivos.write((int) msg.charAt(tamanho++));
            }
        } catch (IOException ex) {
            Logger.getLogger(Config.class.getName()).log(Level.SEVERE, null, ex);
        } finally {
            try {
                escritorArquivos.flush();
            } catch (IOException ex) {
                Logger.getLogger(Config.class.getName()).log(Level.SEVERE, null, ex);
            }
            try {
                escritorArquivos.close();
            } catch (IOException ex) {
                Logger.getLogger(Config.class.getName()).log(Level.SEVERE, null, ex);
            }
        }
        br.com.secharpe.util.MessageCtrl.callMessage("Restart to apply language changes", "Success!!", 1);
        this.dispose();
    }//GEN-LAST:event_jButton1ActionPerformed

